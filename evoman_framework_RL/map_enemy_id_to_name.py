def id_to_name(i):
    if i == 1:
        return "FlashMan"
    if i == 2:
        return "AirMan"
    if i == 3:
        return "WoodMan"
    if i == 4:
        return "HeatMan"
    if i == 5:
        return "MetalMan"
    if i == 6:
        return "CrashMan"
    if i == 7:
        return "BubbleMan"
    if i == 8:
        return "QuickMan"
